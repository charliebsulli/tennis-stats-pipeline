    tourney_level TEXT,
    tourney_date   TEXT,
    match_num INTEGER,
    winner_id INTEGER,
    winner_seed INTEGER,
    winner_entry TEXT,
    winner_name TEXT,
    winner_hand TEXT,
    winner_ht INTEGER,
    winner_ioc TEXT,
    winner_age REAL,
